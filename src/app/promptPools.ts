export function generateCallsignPool(limit = 240): string[] {
  const prompts = ['W1AW', 'G4ABC', 'VK2XYZ']

  for (const prefix of CALLSIGN_PREFIXES) {
    for (let digit = 0; digit <= 9; digit += 1) {
      for (const suffix of CALLSIGN_SUFFIXES) {
        prompts.push(`${prefix}${digit}${suffix}`)
      }
    }
  }

  return normalizePromptPool(prompts).slice(0, limit)
}

export function normalizePromptPool(value: unknown): string[] {
  if (!Array.isArray(value)) return []

  const seen = new Set<string>()
  const prompts: string[] = []
  for (const raw of value) {
    if (typeof raw !== 'string') continue
    const text = raw.toUpperCase().trim()
    if (
      text.length === 0 ||
      seen.has(text) ||
      !containsSupportedChar(text) ||
      !isSupportedPromptText(text)
    ) {
      continue
    }
    seen.add(text)
    prompts.push(text)
  }

  return prompts
}

export function tokenizeCustomText(text: string): string[] {
  return normalizePromptPool(
    text
      .split(/\s+/)
      .map((token) =>
        [...token.toUpperCase()]
          .filter((char) => symbolsFor(char) !== undefined)
          .join(''),
      ),
  )
}

export function samePromptPool(
  a: readonly string[],
  b: readonly string[],
): boolean {
  return a.length === b.length && a.every((prompt, index) => prompt === b[index])
}

function containsSupportedChar(text: string): boolean {
  return [...text].some((char) => symbolsFor(char) !== undefined)
}

function isSupportedPromptText(text: string): boolean {
  return [...text].every(
    (char) => symbolsFor(char) !== undefined || /\s/.test(char),
  )
}
